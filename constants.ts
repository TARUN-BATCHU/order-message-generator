/**
 * A simple CSV parser. Assumes the first row is the header.
 * Handles quoted fields to allow commas within values.
 */
function parseCSV<T>(csvText: string, mapper: (data: {[key: string]: string}) => T): T[] {
    const lines = csvText.trim().split('\n');
    if (lines.length < 2) return [];

    const headers = lines[0].split(',').map(h => h.trim());
    
    return lines.slice(1).map(line => {
        // Basic CSV parsing, doesn't handle commas inside quotes well.
        const values = line.split(',');
        const entry = headers.reduce((obj, header, index) => {
            obj[header] = (values[index] || '').trim().replace(/"/g, '');
            return obj;
        }, {} as {[key: string]: string});
        return mapper(entry);
    });
}

/**
 * Loads and parses the embedded merchants data.
 */
export function loadMerchants(): Merchant[] {
  const merchants = parseCSV(merchantsCsvData, (data) => ({
    id: (data['Company Name'] || '').toLowerCase().replace(/\s+/g, '-'),
    name: data['Company Name'] || '',
    gstin: data['GST Number'] || '',
    address: data['Address'] || '',
    phone: data['Phone'] || '',
    phone2: data['Phone 2'] || '',
  }));

  // Filter out any entries that are effectively empty after parsing
  return merchants.filter(m => m.id && m.name && m.id !== '-');
}

/**
 * Loads and parses the embedded products data.
 */
export function loadProducts(): Product[] {
  const products = parseCSV(productsCsvData, (data) => ({
    id: (data['Product Name'] || '').toLowerCase().replace(/\s+/g, '-'),
    name: data['Product Name'] || '',
  }));
  
  // Filter out any entries that are effectively empty after parsing
  return products.filter(p => p.id && p.name && p.id !== '-');
}
